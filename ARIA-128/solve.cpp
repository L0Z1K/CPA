double double_deviation(double *x, int cnt){
    double avg = 0, square_avg = 0;
    for(int i=0;i<cnt;i++){
        avg += x[i];
        square_avg += x[i]*x[i];
    }
    avg /= cnt;
    square_avg /= cnt;
    return sqrt(square_avg-(avg*avg));
}

double Cov(double *x, double *y, int cnt){
    double avg_x = 0, avg_y = 0, avg_xy = 0;
    for(int i=0;i<cnt;i++){
        avg_x += x[i];
        avg_y += y[i];
        avg_xy += x[i]*y[i];
    }
    avg_x /= cnt;
    avg_y /= cnt;
    avg_xy /= cnt;
    return avg_xy-(avg_x*avg_y);
}

