void Guessing_Key(int idx){
    int real_key = 0;
    double max = 0;
    printf("Guess key[%d] : ", idx);
	for(int key=0;key<=0xff;key++){
        char name[100] = { 0 };
        strcpy(name, result_str);
        sprintf(name+strlen(name), "%d.txt", key);
        FILE *corr_pointer = fopen(name, "wb");

        double HW[TRACES] = { 0 };
		// Calculate hamming weight
		for(int i=0;i<TRACES;i++){
			HW[i] = (double)hamming_weight(S[idx%4][plain[i][idx]^key]);
        }
        // Calculate HW deviation in advance
        double dev_HW = double_deviation(HW, TRACES);

        // Calculate corr sample by sample
        for(int i=0;i<SAMPLES_MAX-SAMPLES_MIN;i++){
            // Calculate TRACE deviation
            double dev_P = double_deviation(sample[i], TRACES);

            // Calculate TRACE X HW deviation
            double Cov_P_HW = Cov(HW, sample[i], TRACES);

            double result = abs(Cov_P_HW / dev_P / dev_HW);
            //printf("%f %f %f => %f\n", dev_HW,dev_P, Cov_P_HW, result);
            fprintf(corr_pointer, "%f\n", result);

            if(result > max){
                max = result;
                real_key = key;
            }
        }
        fclose(corr_pointer);
	}
    printf("%02x %f\n", real_key, max);
}

