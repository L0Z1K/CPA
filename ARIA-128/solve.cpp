int plain[TRACES][20] = { 0 };
double sample[SAMPLES_MAX-SAMPLES_MIN][TRACES] = { 0 };
char trace_str[50] = "problem/trace/";
char plain_str[50] = "problem/data/plain.txt";
char result_str[50] = "problem/result/";

int hamming_weight(int c) {
	int sum = 0;
	while (c != 0) {
		sum += c % 2;
		c = c >> 1;
	}
	return sum;
}

void read_plaintext(void){
    printf("[*] Read Plaintext : ");
	FILE *fp = fopen(plain_str, "rb");
    if(fp==NULL){
        printf("file error\n");
        exit(0);
    }
	for(int i=0;i<TRACES;i++){
		char c[100] = { 0 };
		fgets(c, 100, fp);
		for(int j=0;j<16;j++){
			char n[3] = { 0 };
			n[0] = c[3*j];
			n[1] = c[3*j+1];
			plain[i][j] = strtol(n, NULL, 16);
		}
	}
	fclose(fp);
    printf("Done!\n");
}

